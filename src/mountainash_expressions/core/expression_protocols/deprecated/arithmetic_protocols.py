from __future__ import annotations
from typing import Any, TYPE_CHECKING, Union
from typing import Protocol, runtime_checkable
from enum import Enum, auto
from typing_extensions import TypeAlias


if TYPE_CHECKING:
    from ...types import SupportedExpressions
    from ..expression_nodes import ExpressionNode, ArithmeticExpressionNode, ArithmeticIterableExpressionNode,SupportedArithmeticExpressionNodeTypes
    from ..namespaces import BaseNamespace


class ENUM_ARITHMETIC_OPERATORS(Enum):
    """
    Enumeration for arithmetic operators.

    Attributes:
        - ADD: Addition (+)
        - SUBTRACT: Subtraction (-)
        - MULTIPLY: Multiplication (*)
        - DIVIDE: Division (/)
        - MODULO: Modulo (%)
        - POWER: Exponentiation (**)
        - FLOOR_DIVIDE: Floor division (//)
    """
    SUBTRACT = auto()
    DIVIDE = auto()
    MODULO = auto()
    POWER = auto()
    FLOOR_DIVIDE = auto()

    ADD = auto()
    MULTIPLY = auto()


@runtime_checkable
class ArithmeticVisitorProtocol(Protocol):

    def visit_expression_node( self, node: SupportedArithmeticExpressionNodeTypes) -> SupportedExpressions: ...

    def subtract(self,      node: ArithmeticExpressionNode) -> SupportedExpressions: ...
    def divide(self,        node: ArithmeticExpressionNode) -> SupportedExpressions: ...
    def modulo(self,        node: ArithmeticExpressionNode) -> SupportedExpressions: ...
    def power(self,         node: ArithmeticExpressionNode) -> SupportedExpressions: ...
    def floor_divide(self,  node: ArithmeticExpressionNode) -> SupportedExpressions: ...

    def add(self,       node: ArithmeticIterableExpressionNode) -> SupportedExpressions: ...
    def multiply(self,  node: ArithmeticIterableExpressionNode) -> SupportedExpressions: ...


@runtime_checkable
class ArithmeticExpressionProtocol(Protocol):

    def subtract(self,  left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...
    def divide(self,    left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...
    def modulo(self,    left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...
    def power(self,     left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...
    def floor_divide(self, left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...

    def add(self,       left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...
    def multiply(self,  left: SupportedExpressions, right: SupportedExpressions) -> SupportedExpressions: ...


@runtime_checkable
class ArithmeticBuilderProtocol(Protocol):
    """
    Protocol for arithmetic operations on the builder/namespace layer.

    Includes both named methods and Python dunder operators.
    """

    # Binary operations (self op other)
    def subtract(self, other: Union[BaseNamespace, ExpressionNode, Any]) -> BaseNamespace: ...
    def divide(self, other: Union[BaseNamespace, ExpressionNode, Any]) -> BaseNamespace: ...
    def modulo(self, other: Union[BaseNamespace, ExpressionNode, Any]) -> BaseNamespace: ...
    def power(self, other: Union[BaseNamespace, ExpressionNode, Any]) -> BaseNamespace: ...
    def floor_divide(self, other: Union[BaseNamespace, ExpressionNode, Any]) -> BaseNamespace: ...

    # Reverse binary operations (other op self) - for non-commutative operations
