    def str_starts_with(self,   prefix: Union[ExpressionBuilder,ExpressionNode, Any]) -> ExpressionBuilder: ...
    def str_ends_with(self,     suffix: Union[ExpressionBuilder,ExpressionNode, Any]) -> ExpressionBuilder: ...


